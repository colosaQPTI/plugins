<?php
G::LoadClass("plugin");

class limousinPlanMonExpDashletPlugin extends PMPlugin
{
  private $dashletsUids;
  
  public function limousinPlanMonExpDashletPlugin($sNamespace, $sFilename = null)
  {
    $res = parent::PMPlugin($sNamespace, $sFilename);
    $this->sFriendlyName = "limousinPlanMonExpDashlet Plugin";
    $this->sDescription  = "Autogenerated plugin for class limousinPlanMonExpDashlet";
    $this->sPluginFolder = "limousinPlanMonExpDashlet";
    $this->sSetupPage    = "setup";
    $this->iVersion      = 1;
    //$this->iPMVersion    = 2425;
    $this->aWorkspaces   = null;
    //$this->aWorkspaces = array("os");
    
    
    $this->dashletsUids = array(
      array("DAS_UID" => "81266480252a6ea4668e720003862680",
            "DAS_CLASS" => "dashletlimousinPlanMonExpDashlet",
            "DAS_TITLE" => "Dashlet limousinPlanMonExpDashlet",
            "DAS_DESCRIPTION" => "Dashlet limousinPlanMonExpDashlet",
            "DAS_VERSION" => "1.0",
            "DAS_CREATE_DATE" => date("Y-m-d"),
            "DAS_UPDATE_DATE" => date("Y-m-d"))
    );
    
    
    return $res;
  }

  public function setup()
  {
    $this->registerMenu("processmaker", "menulimousinPlanMonExpDashlet.php");
    $this->registerPmFunction();
    
    $this->registerDashlets();
  }

  public function install()
  {
  }
  
  public function enable()
  {
    $this->dashletInsert();
  }

  public function disable()
  {
    $this->dashletDelete();
  }
  
  public function dashletInsert()
  {
    require_once ("classes/model/Dashlet.php");
    
    $connection = Propel::getConnection(DashletPeer::DATABASE_NAME);
    
    foreach ($this->dashletsUids as $dashletData) {
      $dashlet = new Dashlet();
      
      if (is_null($dashlet->load($dashletData["DAS_UID"]))) {
        $dashlet->fromArray($dashletData, BasePeer::TYPE_FIELDNAME);
        $connection->begin();
        $dashlet->save();
        $connection->commit();
      }
    }
  }
  
  public function dashletDelete()
  {
    require_once ("classes/model/Dashlet.php");
    
    foreach ($this->dashletsUids as $dashletData) {
      $criteria = new Criteria("workflow");
      $criteria->add(DashletPeer::DAS_UID, $dashletData["DAS_UID"]);
      DashletPeer::doDelete($criteria);
    }
  }
}

$oPluginRegistry = &PMPluginRegistry::getSingleton();
$oPluginRegistry->registerPlugin("limousinPlanMonExpDashlet", __FILE__);
