  <td colspan="2" style="font-family: Helvetica;font-size: 12pt;font-weight: bold;">What is "ProcessMaker in Outlook"?</td>
</tr>
<tr>
  <td colspan="2" valign="top" style="font-family: Helvetica;font-size: 10pt;">
  <p align="justify">
    "ProcessMaker in Outlook" is a plugin created to allow standard ProcessMaker users to use ProcessMaker Open Source BPM & Workflow from within the Microsoft Outlook email client (2007 and 2010) instead of through the standard browser interface. 
  </p>
  </td>
</tr>
