<tr height="40">
  <td colspan="2" valign="top" style="font-family: Helvetica;font-size: 12pt;font-weight: bold;">What are the features of this plugin?</td>
</tr>
<tr>
  <td colspan="2" style="font-family: Helvetica;font-size: 10pt;">
  <p align="justify">
    This plugin allows users to perform all common tasks from within the Outlook interface including the following:
<br />
<ol>
	<li>Start a New Case                                  </li>
	<li>Review and attend to existing Cases in the Inbox  </li>
	<li>Save Drafts                                       </li>
	<li>Review Participated, Paused, and Unassigned Cases </li>
	<!-- <li>Review attached documents in DMS tree             </li>
	<li>Perform Process Supervisor functions              </li>
	<li>Search                                            </li> -->
</ol>	
All tasks are performed in the same way they are performed through the web browser.  Designers and Administrators should still enter via a web browser to perform their respective tasks.
