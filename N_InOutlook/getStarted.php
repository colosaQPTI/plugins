</tr>
<tr>
<td colspan="2" style="font-family: Helvetica;font-size: 10pt;">
  <p align="justify">
    The plugin creates a new command bar in Outlook with one option: "Configuration".  The Configuration option allows the user to change the data connection to the server where ProcessMaker is installed.  This is the same screen that must be configured when the plugin is initially installed.
