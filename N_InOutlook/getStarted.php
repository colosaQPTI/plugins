There is an additional new feature which has been added to this plugin which is not available in ProcessMaker via a web browser.  In the Outlook integration a case can be started based on an email that is received in a specific Outlook folder.  When this feature is configured, the ProcessMaker outlook plugin will poll received cases and then extract relevant data (from, subject, body, etc.).  ProcessMaker will then create a case and pass this extracted data into ProcessMaker variables as pertinent metadata.   To see more details click on the folder "Inbox/ProcessMaker/Autopilot". -->
  </p>
