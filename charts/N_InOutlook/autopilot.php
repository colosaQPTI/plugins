  <p align="justify">
    To configure the Autopilot functionality follow these steps: 
<br /><br />
<ol>
	<li>
Create one or more new folders in the folder "ProcessMaker/Autopilot/{MyFolder}" where "MyFolder" is the name that you wish to place this folder.
</li><li>
Create a new classification rule for one or more new folders created, this rule is the one that will be the responsible for sorting incoming emails to be processed.
</li><li>
Click on the "Autopilot" option from the ProcessMaker command bar.
</li><li>
Add a new row to the grid that is displayed in the window that opens.
</li><li>
Select a folder that appears in the list and the task in which a new case can be started.
</li><li>
Repeat steps 3 and 4 for each task in which a cases can be started
</li><li>
Save the changes
</li>
</ol>
</br>
The folder "Processed Cases" belongs to the plugin and it will store all the "emails" that have been processed by the Autopilot.
