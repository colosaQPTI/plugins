CREATE TABLE IF NOT EXISTS `DIM_TIME_COMPLETE`
(
	`TIME_ID` VARCHAR(10) default '' NOT NULL,
	`MONTH_ID` INTEGER default 0 NOT NULL,
	`QTR_ID` INTEGER default 0 NOT NULL,
	`YEAR_ID` INTEGER default 0 NOT NULL,
	`MONTH_NAME` VARCHAR(3) default '0' NOT NULL,
	`MONTH_DESC` VARCHAR(15) default '' NOT NULL,
	`QTR_NAME` VARCHAR(4) default '' NOT NULL,
	`QTR_DESC` VARCHAR(15) default '' NOT NULL,
	PRIMARY KEY (`TIME_ID`)
)Type=MyISAM  DEFAULT CHARSET='utf8' COMMENT='The DIM_TIME_COMPLETE';

TRUNCATE DIM_TIME_COMPLETE;

insert into DIM_TIME_COMPLETE
SELECT
  DISTINCT SUBSTRING(DEL_FINISH_DATE,1,10),
  MONTH(DEL_FINISH_DATE),
  QUARTER(DEL_FINISH_DATE),
  YEAR(DEL_FINISH_DATE),
  DATE_FORMAT ( DEL_FINISH_DATE, '%b'),
  DATE_FORMAT ( DEL_FINISH_DATE, '%M %Y'),
  CONCAT( 'QTR', QUARTER(DEL_FINISH_DATE) ),
  CONCAT( 'Quarter ', QUARTER(DEL_FINISH_DATE), ' ', YEAR(DEL_FINISH_DATE) )
FROM APP_CACHE_VIEW
WHERE SUBSTRING(DEL_FINISH_DATE,1,10) != 'NULL';
